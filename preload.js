const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('electronAPI', {
  minimize:            ()    => ipcRenderer.send('window-minimize'),
  maximize:            ()    => ipcRenderer.send('window-maximize'),
  close:               ()    => ipcRenderer.send('window-close'),
  launchExe:           (exePath, gameId) => ipcRenderer.invoke('launch-exe', { exePath, gameId }),
  killGame:            (gameId)          => ipcRenderer.invoke('kill-game', gameId),
  onNativeContextMenu: (cb) => ipcRenderer.on('native-context-menu', cb),
  showGameCtxMenu:     (data) => ipcRenderer.invoke('show-game-ctx-menu', data),
  onCtxAction:         (cb)  => ipcRenderer.on('ctx-action', cb),
  openFolder:          (p)   => ipcRenderer.invoke('open-folder', p),
  browseExe:           ()    => ipcRenderer.invoke('browse-exe'),
  browseImage:         ()    => ipcRenderer.invoke('browse-image'),
  browseSaveFolder:    ()    => ipcRenderer.invoke('browse-save-folder'),
  getSaveInfo:         (p)   => ipcRenderer.invoke('get-save-info', p),
  zipSaves:            (p,n) => ipcRenderer.invoke('zip-saves', p, n),
  getCloudSettings:    ()    => ipcRenderer.invoke('get-cloud-settings'),
  saveCloudSettings:   (s)   => ipcRenderer.invoke('save-cloud-settings', s),
  openCloudWindow:     (lang) => ipcRenderer.invoke('open-cloud-window', lang),
  uploadToCloud:       (d)   => ipcRenderer.invoke('upload-to-cloud', d),
  listCloudBackups:    (d)   => ipcRenderer.invoke('list-cloud-backups', d),
  downloadCloudBackup: (d)   => ipcRenderer.invoke('download-cloud-backup', d),
  restoreBackup:       (d)   => ipcRenderer.invoke('restore-backup', d),
  ensureSavePath:      (p)   => ipcRenderer.invoke('ensure-save-path', p),
  deleteCloudBackup:   (d)   => ipcRenderer.invoke('delete-cloud-backup', d),
  purgeFtpSaves:       (d)   => ipcRenderer.invoke('purge-ftp-saves', d),
  taskStatus:          ()    => ipcRenderer.invoke('task-status'),
  taskCreate:          ()    => ipcRenderer.invoke('task-create'),
  taskRemove:          ()    => ipcRenderer.invoke('task-remove'),
  oauthStart:          (d)   => ipcRenderer.invoke('oauth-start', d),
  oauthRefresh:        (d)   => ipcRenderer.invoke('oauth-refresh', d),
  scanSaveFolder:      (n)   => ipcRenderer.invoke('scan-save-folder', n),
  purgeFtpBackups:     (d)   => ipcRenderer.invoke('purge-ftp-backups', d),
  backupConfigToFtp:   (d)   => ipcRenderer.invoke('backup-config-to-ftp', d),
  purgeFtpConfigs:     (d)   => ipcRenderer.invoke('purge-config-backups', d),
  listConfigBackups:   (d)   => ipcRenderer.invoke('list-config-backups', d),
  downloadConfigBackup:(d)   => ipcRenderer.invoke('download-config-backup', d),
  onCloudUpdated: (cb) => ipcRenderer.on('cloud-settings-updated', cb),
  notify:              (d)   => ipcRenderer.invoke('show-notification', d),
  onGameExited:   (cb) => ipcRenderer.on('game-exited', cb),
  onWindowMaximized: (cb) => ipcRenderer.on('window-maximized', (_, v) => cb(v)),
  openExternal:      (url) => ipcRenderer.invoke('open-external', url),
  checkUpdate:       ()    => ipcRenderer.invoke('check-update'),
  writeClipboard:    (text) => ipcRenderer.invoke('write-clipboard', text),
  getAppVersion:     ()    => ipcRenderer.invoke('get-app-version'),
  exportConfig:      (json) => ipcRenderer.invoke('export-config', json),
  importConfig:      ()    => ipcRenderer.invoke('import-config'),
  writeActivityLog:  (msg)  => ipcRenderer.invoke('write-activity-log', msg),
  readActivityLog:   ()     => ipcRenderer.invoke('read-activity-log'),
  openLogWindow:     (lang) => ipcRenderer.invoke('open-log-window', lang),
  confirmQuit:       ()     => ipcRenderer.invoke('confirm-quit'),
  onBeforeExit:      (cb)   => ipcRenderer.on('before-exit', cb),
});
